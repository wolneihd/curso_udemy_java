package secao_10_arrays_listas;

public class aula_87 {
    public static void main(String[] args) {

        /**
         * tipo referência: 
         *  - variáveis são ponteiros.
         *  - objetos precisam ser instanciados usando new.
         *  - Y = X => Y passa a apontar pra o onde X aponta.
         * 
         * tipo primitivo:
         *  - mais simples e mais performático.
         *  - Y = X => Y recebe uma cópia de X.
         *  - não aceita valor NULL. 
         */

        int p = 10;
        System.out.println(p); // int p; The local variable p may not have been initialized.

    }
}
